export async function seedArticles(): Promise<void> {
  console.log('Seeding articles...');
  
  try {
    // Check if articles already exist
    const existingCount = await Article.countDocuments();
    if (existingCount > 0) {
      console.log(`Articles already exist (${existingCount} found). Skipping seed.`);
      return;
    }
    
    // Insert sample articles
    const articles = await Article.insertMany(sampleArticles);
    console.log(`Successfully seeded ${articles.length} articles`);
    
    // Update related articles for cross-referencing
    const allArticles = await Article.find({}).select('_id category');
    
    for (const article of articles) {
      const relatedArticles = allArticles
        .filter(a => a.category === article.category && a._id.toString() !== article._id.toString())
        .slice(0, 3)
        .map(a => a._id);
      
      if (relatedArticles.length > 0) {
        await Article.findByIdAndUpdate(article._id, { relatedArticles });
      }
    }
    
    console.log('Articles seeded and cross-referenced successfully');
  } catch (error) {
    console.error('Error seeding articles:', error);
    throw error;
  }
}